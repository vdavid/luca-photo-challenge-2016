    <p>No de hogyan is kell ilyen képeket készíteni?</p>

    <ol>
        <li><strong>Fényképezési mód</strong><br />Az exponálási idő kézi beállításához a gépet - mint tudod :) - "Tv" vagy "M" módba kell tenni. A "Tv" egyszerűbben használható, és jelen esetben ugyanolyan jó, mint az "M'.</li>
        <li><strong>Állvány</strong><br />Hogy az álló dolgok élesek legyenek, mindenképp használj állványt vagy legalább egy pulcsit, vagy valami helyet, ahová stabilan le tudod rakni a gépet.</li>
        <li><strong>Önkioldó</strong><br />Bármilyen óvatosan is nyomod le az exponálógombot, a gép egy picit remegni fog, ettől pedig elmosódik a kép. Emiatt érdemes az önkioldót használni, és 2 másodpercre állítani. Vagy ha te akarsz bohóckodni a képen, akkor többre, hogy odaérj :)</li>
    </ol>

